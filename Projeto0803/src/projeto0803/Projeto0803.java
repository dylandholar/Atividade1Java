package projeto0803;

public class Projeto0803 {

    public static void main(String[] args) {

        byte a = (byte) 116;

        System.out.println("Variável tipo byte: " + a);
        
        short b = 117;

        System.out.println("Variável tipo short: " + b);
        
        int c = 118;

        System.out.println("Variável tipo int: " + c);
        
        long d = 119;

        System.out.println("Variável tipo long: " + d);
        
        float e = 120.1f;

        System.out.println("Variável tipo float: " + e);
        
        double f = 121.1;
        
        System.out.println("Variável tipo double: " + f);
        
        boolean g = true;

        System.out.println("Variável tipo boolean: " + g);
        
        char h = 'D';

        System.out.println("Variável tipo char: " + h);

      
        
        
    }

}
